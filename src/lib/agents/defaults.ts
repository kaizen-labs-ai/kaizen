/**
 * Ensures system agent configs exist in the DB, seeding defaults on first access.
 * System agent prompts are always kept in sync with code defaults — users
 * cannot customise them. System agents are always enabled.
 * User-tunable fields (model, thinking, timeout) are preserved across restarts.
 *
 * Only manages type="system" rows. Future type="user" (sub-agents) are
 * left untouched.
 *
 * Memoized with a 30s TTL — subsequent calls within the window are no-ops.
 * Call `invalidateAgentConfigCache()` after settings changes to force a re-check.
 */
let _agentConfigsLastSync = 0;
const AGENT_CONFIGS_TTL_MS = 30_000;

export function invalidateAgentConfigCache() {
  _agentConfigsLastSync = 0;
}

export async function ensureAgentConfigs() {
  const now = Date.now();
  if (now - _agentConfigsLastSync < AGENT_CONFIGS_TTL_MS) return;
  _agentConfigsLastSync = now;
  const existing = await prisma.agentConfig.findMany({
    where: { type: "system" },
  });
  const existingMap = new Map(existing.map((e) => [e.id, e]));

  // Remove stale system agents no longer in defaults
  for (const e of existing) {
    if (!SYSTEM_AGENT_IDS.has(e.id)) {
      await prisma.agentConfig.delete({ where: { id: e.id } });
    }
  }

  for (const agent of AGENT_DEFAULTS) {
    const ex = existingMap.get(agent.id);

    if (!ex) {
      // ── New agent: create with code defaults ──
      await prisma.agentConfig.create({
        data: {
          id: agent.id,
          type: "system",
          label: agent.label,
          model: agent.model,
          imageModel: agent.imageModel ?? null,
          fileModel: agent.fileModel ?? null,
          audioModel: agent.audioModel ?? null,
          videoModel: agent.videoModel ?? null,
          thinking: agent.thinking,
          timeout: agent.timeout,
          enabled: true,
          systemPrompt: agent.systemPrompt,
          customPrompt: null,
          promptVersion: agent.promptVersion,
        },
      });
      continue;
    }

    // ── Existing system agent: always sync prompt + force enabled ──
    const updates: Record<string, unknown> = {};

    // System agents are always enabled
    if (!ex.enabled) {
      updates.enabled = true;
    }

    if (ex.promptVersion !== agent.promptVersion || ex.systemPrompt !== agent.systemPrompt) {
      updates.systemPrompt = agent.systemPrompt;
      updates.customPrompt = null;
      updates.promptVersion = agent.promptVersion;
    }

    // Sync timeout: if still at column default (120) and code default differs
    if (ex.timeout === 120 && agent.timeout !== 120) {
      updates.timeout = agent.timeout;
    }

    // Sync media models: if code defines them but DB has nulls (new multimodal capability)
    if (agent.imageModel && !ex.imageModel) updates.imageModel = agent.imageModel;
    if (agent.audioModel && !ex.audioModel) updates.audioModel = agent.audioModel;
    if (agent.videoModel && !ex.videoModel) updates.videoModel = agent.videoModel;
    if (agent.fileModel && !ex.fileModel) updates.fileModel = agent.fileModel;

    if (Object.keys(updates).length > 0) {
      await prisma.agentConfig.update({
        where: { id: agent.id },
        data: updates,
      });
    }
  }
}
